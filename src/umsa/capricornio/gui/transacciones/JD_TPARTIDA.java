    void CerearTabla(){
        int f = JTPartida.getRowCount();
        for (int i=f-1;i>=0;i--){
             if (partida.delete(i)) {
                JTPartida.tableChanged(new TableModelEvent(
                partida, i, i, TableModelEvent.ALL_COLUMNS, TableModelEvent.INSERT));
             }
        }
    }
    private void LlenaTabla(){        
        try{
            
            AdquiWSServiceLocator servicio = new AdquiWSServiceLocator();
            AdquiWS_PortType puerto = servicio.getAdquiWS();
            
            Map[] datos=puerto.getTotalPartidas(cod_trans_nro);
//            Map[] datos = null;
            CerearTabla();
            double total = 0;
            if (datos!=null){
                for (int c=0;c<datos.length;c++){
                    partida.insert(c);
                    JTPartida.tableChanged(new TableModelEvent(partida, c, c, TableModelEvent.ALL_COLUMNS,TableModelEvent.INSERT));
                    JTPartida.setValueAt(datos[c].get("PARTIDA"),c,0);
                    JTPartida.setValueAt(datos[c].get("TOTAL"),c,1);
                    total = total + Double.parseDouble(datos[c].get("TOTAL").toString());
                    
                }
                this.JTTotal.setText(String.valueOf(total));
//                this.TxtTotal.setText(String.valueOf(sum));
            }
            
        }
        catch (RemoteException e){
            javax.swing.JOptionPane.showMessageDialog(this,"<html> error de conexion con el servidor <br> "+e,"SYSTEM CAPRICORN",
                        javax.swing.JOptionPane.ERROR_MESSAGE);
        }
        catch (ServiceException e){ System.out.println(e);}        
    }

