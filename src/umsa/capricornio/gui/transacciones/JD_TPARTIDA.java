    private void ConstruyeTablaTPartidas(){
        
        partida = new TablaTotalPartida();
        JTPartida.setAutoCreateColumnsFromModel(false);
        JTPartida.setModel(partida);
        
        
        for (int k = 0; k < TablaTotalPartida.m_columns.length; k++) {
            TableCellRenderer renderer = new MiRenderer(TablaTotalPartida.m_columns[k].m_alignment);
           
            TableColumn column = new TableColumn(k, TablaTotalPartida.m_columns[k].m_width, renderer, null);
            JTPartida.addColumn(column);
        }
        JTableHeader header = JTPartida.getTableHeader();
        header.setUpdateTableInRealTime(true);
        header.setReorderingAllowed(true);
        this.JSTPartida.getViewport().add(JTPartida);
    }

