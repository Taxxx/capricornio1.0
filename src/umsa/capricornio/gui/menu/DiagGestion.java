    private void BtnAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnAceptarActionPerformed
        if (!validacion.valNumero(TxtGestion.getText(), 4, 4)) {
             javax.swing.JOptionPane.showMessageDialog(this,"Debe introducir solo numeros en la gestion","SYSTEM CAPRICORN SYSTEM",
             javax.swing.JOptionPane.ERROR_MESSAGE); 
             TxtGestion.requestFocus();
        }
        try {
            AdquiWSServiceLocator servicio = new AdquiWSServiceLocator();
            AdquiWS_PortType puerto = servicio.getAdquiWS();

            Map[] datos=puerto.getGestion(Integer.parseInt(TxtGestion.getText()));
            if (datos!=null){
                for (int c=0;c<datos.length;c++){                    
                    gestion=Integer.parseInt(datos[c].get("GESTION").toString());
                }
                BtnCancelar.doClick();
            }
            else { javax.swing.JOptionPane.showMessageDialog(this,"LA GESTION NO ESTA HABILITADA","CAJA PAGADORA",
                   javax.swing.JOptionPane.ERROR_MESSAGE);                    
            }
        }
        catch (RemoteException e){
            javax.swing.JOptionPane.showMessageDialog(this,"<html> error de conexion con el servidor <br> "+e,"CAJA PAGADORA",
                        javax.swing.JOptionPane.ERROR_MESSAGE);
        }
        catch (ServiceException e){ System.out.println(e);}
    }//GEN-LAST:event_BtnAceptarActionPerformed

